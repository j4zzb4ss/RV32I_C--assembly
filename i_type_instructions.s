i_type_operations:
        # ПРОЛОГ ФУНКЦИИ - подготовка стекового кадра
        addi    sp,sp,-48        # Выделяем 48 байт в стеке
        sw      s0,44(sp)        # Сохраняем старый frame pointer
        addi    s0,sp,48         # Устанавливаем новый frame pointer (s0 = sp + 48)
        sw      a0,-36(s0)       # Сохраняем параметр a (первый аргумент) в стек [s0-36]
        sw      a1,-40(s0)       # Сохраняем параметр ptr (второй аргумент) в стек [s0-40]
        sw      zero,-20(s0)     # result = 0 (инициализация локальной переменной [s0-20])

        # ОПЕРАЦИЯ 1: ADDI - сложение с immediate
        # C код: result = a + 42
        lw      a5,-36(s0)        # a5 = a (загружаем значение 'a' из стека)
        addi    a5,a5,42          # a5 = a5 + 42 (ADDI - складываем с константой 42)
        sw      a5,-20(s0)        # сохраняем результат в переменную result [s0-20]

        # ОПЕРАЦИЯ 2: ANDI - побитовое И с immediate
        # C код: result = a & 0xFF
        lw      a5,-36(s0)        # a5 = a
        andi    a5,a5,255         # a5 = a5 & 255 (ANDI - 255 = 0xFF)
        sw      a5,-20(s0)        # сохраняем в result

        # ОПЕРАЦИЯ 3: ORI - побитовое ИЛИ с immediate
        # C код: result = a | 0xF0
        lw      a5,-36(s0)        # a5 = a
        ori     a5,a5,240         # a5 = a5 | 240 (ORI - 240 = 0xF0)
        sw      a5,-20(s0)        # сохраняем в result

        # ОПЕРАЦИЯ 4: XORI - побитовое исключающее ИЛИ с immediate
        # C код: result = a ^ 0xAAAA
        # Компилятор разбил 0xAAAA (43690) на две части: 45056 (0xB000) - 1366 (0x556)
        # 0xB000 - 0x556 = 0xAAAA
        lw      a4,-36(s0)        # a4 = a
        li      a5,45056          # a5 = 45056 (0xB000) - загружаем верхнюю часть
        addi    a5,a5,-1366       # a5 = 45056 - 1366 = 43690 (0xAAAA)
        xor     a5,a4,a5          # a5 = a4 XOR a5 (XORI - исключающее ИЛИ с 0xAAAA)
        sw      a5,-20(s0)        # сохраняем в result

        # ОПЕРАЦИЯ 5: SLLI - логический сдвиг влево с immediate
        # C код: result = a << 3
        lw      a5,-36(s0)        # a5 = a
        slli    a5,a5,3           # a5 = a5 << 3 (SLLI - сдвиг влево на 3 бита)
        sw      a5,-20(s0)        # сохраняем в result

        # ОПЕРАЦИЯ 6: SRAI - арифметический сдвиг вправо с immediate
        # C код: result = a >> 2 (для signed int - арифметический сдвиг)
        lw      a5,-36(s0)        # a5 = a
        srai    a5,a5,2           # a5 = a5 >> 2 (SRAI - арифметический сдвиг вправо на 2)
        sw      a5,-20(s0)        # сохраняем в result

        # ОПЕРАЦИЯ 7: LW - загрузка слова из памяти со смещением
        # C код: result = ptr[5] (доступ к 6-му элементу массива)
        lw      a5,-40(s0)        # a5 = ptr (загружаем указатель из стека)
        lw      a5,20(a5)         # a5 = *(ptr + 20) - LW со смещением 20 байт (5 * 4)
        sw      a5,-20(s0)        # сохраняем в result

        # ЭПИЛОГ ФУНКЦИИ - возврат результата
        lw      a5,-20(s0)        # a5 = result (финальное значение)
        mv      a0,a5             # a0 = a5 (готовим возвращаемое значение)
        lw      s0,44(sp)         # восстанавливаем старый frame pointer
        addi    sp,sp,48          # освобождаем стек
        jr      ra                # возвращаемся в вызывающую функцию (jalr)
