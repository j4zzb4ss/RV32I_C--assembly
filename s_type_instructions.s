s_type_operations:
        # ПРОЛОГ ФУНКЦИИ - подготовка стекового кадра
        addi    sp,sp,-48        # Выделяем 48 байт в стеке
        sw      s0,44(sp)        # Сохраняем старый frame pointer
        addi    s0,sp,48         # Устанавливаем новый frame pointer
        sw      a0,-36(s0)       # Сохраняем параметр arr (указатель) в стек [s0-36]
        sw      a1,-40(s0)       # Сохраняем параметр value в стек [s0-40]

        # ОПЕРАЦИЯ 1: SW - сохранение слова со смещением 0
        # C код: arr[0] = value
        lw      a5,-36(s0)        # a5 = arr (загружаем указатель из стека)
        lw      a4,-40(s0)        # a4 = value (загружаем значение)
        sw      a4,0(a5)          # SW: память[arr + 0] = value

        # ОПЕРАЦИЯ 2: SW - сохранение слова со смещением 20
        # C код: arr[5] = value + 1
        lw      a5,-36(s0)        # a5 = arr
        addi    a5,a5,20          # a5 = arr + 20 (вычисляем адрес arr[5])
        lw      a4,-40(s0)        # a4 = value
        addi    a4,a4,1           # a4 = value + 1
        sw      a4,0(a5)          # SW: память[arr + 20] = value + 1

        # Подготовка к работе с short (полуслова)
        # C код: short *sptr = (short*)arr;
        lw      a5,-36(s0)        # a5 = arr
        sw      a5,-20(s0)        # сохраняем sptr (тот же адрес) в локальную переменную [s0-20]

        # ОПЕРАЦИЯ 3: SH - сохранение полуслова
        # C код: sptr[2] = value; (sptr[2] на смещении 4 байта)
        lw      a5,-20(s0)        # a5 = sptr
        addi    a5,a5,4           # a5 = sptr + 4 (адрес sptr[2])
        lw      a4,-40(s0)        # a4 = value
        slli    a4,a4,16          # Расширяем знак для short (старшие 16 бит)
        srai    a4,a4,16          # Получаем знаковое 16-битное значение
        sh      a4,0(a5)          # SH: память[sptr + 4] = (short)value

        # Подготовка к работе с char (байты)
        # C код: char *cptr = (char*)arr;
        lw      a5,-36(s0)        # a5 = arr
        sw      a5,-24(s0)        # сохраняем cptr в локальную переменную [s0-24]

        # ОПЕРАЦИЯ 4: SB - сохранение байта
        # C код: cptr[3] = value;
        lw      a5,-24(s0)        # a5 = cptr
        addi    a5,a5,3           # a5 = cptr + 3 (адрес cptr[3])
        lw      a4,-40(s0)        # a4 = value
        andi    a4,a4,0xff        # a4 = value & 0xFF (берем только младший байт)
        sb      a4,0(a5)          # SB: память[cptr + 3] = (char)value

        # ЭПИЛОГ ФУНКЦИИ
        nop                       # пустая операция (возможно, для отладки)
        lw      s0,44(sp)         # восстанавливаем старый frame pointer
        addi    sp,sp,48          # освобождаем стек
        jr      ra                # возвращаемся в вызывающую функцию
